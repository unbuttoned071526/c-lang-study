#include <stdio.h>

int main(void)
{
    char charNumber = 'F';
    int intNumber = 10;
    float floatNumber = 3.14f;
    double doubleNumber = 3.14159;

    printf("charNumber: %c.\n", charNumber);
    printf("intNumber: %d.\n", intNumber);
    printf("floatNumber: %f.\n", floatNumber);
    printf("doubleNumber: %lf.\n", doubleNumber);

    return 0;
}
